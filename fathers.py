def get_readings_for_father(name):
    readings = []
    for reading in FATHER_READINGS.get(name, []):
        source = SOURCE_LINKS.get(reading["title"])
        readings.append(
            {
                **reading,
                "source_url": source["url"] if source else None,
                "source_label": source["label"] if source else None,
            }
        )
    return readings


